package ru.levelup.qa.at.unit.tests;

public class Calculator {

    public double sum(int a, int b) {
        return a + b;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public double divide(double a, double b) {
        return a / b;
    }

    public int divideInt(int a, int b) {
        return a / b;
    }
}
